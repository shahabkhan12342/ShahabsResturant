function addToCart()
{
    var img1=document.getElementById('img1');
    var name1=document.getElementById('name1')
    var price1=document.getElementById('price1')

    var cartimg1=document.getElementById('cart-img1');
    var cartname1=document.getElementById('cart-name1')
    var cartprice1=document.getElementById('cart-price1')
    
    cartimg1.src=img1.src;
    cartname1.innerHTML=name1.innerHTML;
    cartprice1.innerHTML=price1.innerHTML;
}
